    [MenuItem("Tools/Week/Generate 5-Week Validation Sample SO")]
    public static void GenerateFromMenu()
    {
        GenerateInternal();
        EditorUtility.DisplayDialog(
            "Week Validation Samples",
            "5주 검증용 샘플 SO를 Assets/Data/Test/WeekValidationSamples 에 생성했습니다.",
            "확인");
