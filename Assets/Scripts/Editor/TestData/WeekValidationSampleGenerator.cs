    private static SO_WeekDefinition CreateWeekDefinition(
        string relativeFolder,
        string fileName,
        string id,
        int weekIndex,
        string title,
        string summary,
        SO_CardInteractionDefinition[] onWeekStartInteractions,
        SO_CardInteractionDefinition[] onWeekEndInteractions,
        SO_CardInfoDefinition[] cards,
        SO_DayRoutineEventDefinition[] routineEvents,
        SO_StoryEventDefinition[] storyEvents,
        SO_PrivateDialogueDefinition[] nightEvents)
    {
        SO_WeekPreTurnDefinition preTurn = CreateAsset<SO_WeekPreTurnDefinition>(relativeFolder, $"WeekPreTurn_{fileName}", $"{title}_PreTurn");
        SetField(preTurn, "_title", $"{title} / PreTurn");
        SetField(preTurn, "_summary", summary);
        SetField(preTurn, "_informationCards", BuildWeekCardEntries(cards));

        SO_WeekDayFlowDefinition dayFlow = CreateAsset<SO_WeekDayFlowDefinition>(relativeFolder, $"WeekDayFlow_{fileName}", $"{title}_DayFlow");
        SetField(dayFlow, "_routineEvents", routineEvents ?? Array.Empty<SO_DayRoutineEventDefinition>());
        SetField(dayFlow, "_storyEvents", storyEvents ?? Array.Empty<SO_StoryEventDefinition>());

        SO_WeekNightFlowDefinition nightFlow = CreateAsset<SO_WeekNightFlowDefinition>(relativeFolder, $"WeekNightFlow_{fileName}", $"{title}_NightFlow");
        SetField(nightFlow, "_dialogues", nightEvents ?? Array.Empty<SO_PrivateDialogueDefinition>());

        SO_WeekDefinition week = CreateAsset<SO_WeekDefinition>(relativeFolder, fileName, title);
        SetField(week, "_id", id);
        SetField(week, "_weekIndex", weekIndex);
        SetField(week, "_title", title);
        SetField(week, "_summary", summary);
        SetField(week, "_preTurn", preTurn);
        SetField(week, "_dayFlow", dayFlow);
        SetField(week, "_nightFlow", nightFlow);
        SetField(week, "_weekRules", Array.Empty<SO_WeekRuleDefinition>());
        SetField(week, "_onWeekStartInteractions", onWeekStartInteractions ?? Array.Empty<SO_CardInteractionDefinition>());
        SetField(week, "_onWeekEndInteractions", onWeekEndInteractions ?? Array.Empty<SO_CardInteractionDefinition>());
        SetField(week, "_cinematicProfile", null);

        EditorUtility.SetDirty(preTurn);
        EditorUtility.SetDirty(dayFlow);
        EditorUtility.SetDirty(nightFlow);
        EditorUtility.SetDirty(week);
        return week;
    }

