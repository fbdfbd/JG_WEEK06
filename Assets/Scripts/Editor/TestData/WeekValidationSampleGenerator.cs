    private static T CreateAsset<T>(string relativeFolder, string fileName, string assetName)
        where T : ScriptableObject
    {
        string folderPath = $"{OutputRoot}/{relativeFolder}".Replace("\\", "/");
        EnsureFolder(folderPath);

        string assetPath = $"{folderPath}/{fileName}".Replace("\\", "/");
        T asset = ScriptableObject.CreateInstance<T>();
        asset.name = assetName;
        AssetDatabase.CreateAsset(asset, assetPath);
        return asset;
    }

    private static T LoadRequired<T>(string assetPath) where T : UnityEngine.Object
    {
        T asset = AssetDatabase.LoadAssetAtPath<T>(assetPath);
        if (asset == null)
        {
            throw new InvalidOperationException($"Required asset is missing: {assetPath}");
        }

        return asset;
    }

    private static void ResetOutputRoot()
    {
        if (AssetDatabase.IsValidFolder(OutputRoot))
        {
            FileUtil.DeleteFileOrDirectory(OutputRoot);
            FileUtil.DeleteFileOrDirectory($"{OutputRoot}.meta");
        }
    }

    private static void EnsureFolder(string folderPath)
    {
        folderPath = folderPath.Replace("\\", "/");
        string[] parts = folderPath.Split('/');
        if (parts.Length == 0)
        {
            return;
        }

        string current = parts[0];
        for (int i = 1; i < parts.Length; i++)
        {
            string next = $"{current}/{parts[i]}";
            if (!AssetDatabase.IsValidFolder(next))
            {
                AssetDatabase.CreateFolder(current, parts[i]);
            }

            current = next;
        }
    }

