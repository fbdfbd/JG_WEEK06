    public static void GenerateBatch()
    {
        GenerateInternal();
        Debug.Log("[WeekValidationSampleGenerator] Generation completed.");
    }

    [InitializeOnLoadMethod]
    private static void AutoGenerateInOpenEditor()
    {
        if (Application.isBatchMode)
        {
            return;
        }

        if (SessionState.GetBool(AutoGenerateSessionKey, false))
        {
            return;
        }

        SessionState.SetBool(AutoGenerateSessionKey, true);
        EditorApplication.delayCall += () =>
        {
            try
            {
                GenerateInternal();
                Debug.Log("[WeekValidationSampleGenerator] Auto-generated validation sample assets.");
            }
            catch (Exception exception)
            {
                Debug.LogException(exception);
            }
        };
    }

    private static void GenerateInternal()
    {
        SharedAssets shared = LoadSharedAssets();

        ResetOutputRoot();
        EnsureFolder(OutputRoot);
        EnsureFolder($"{OutputRoot}/Common");
        EnsureFolder($"{OutputRoot}/Common/Interactions");
        EnsureFolder($"{OutputRoot}/Week");

        TestInteractions interactions = CreateCommonInteractions(shared);

        SO_WeekDefinition week1 = CreateWeek1(shared, interactions);
        SO_WeekDefinition week2 = CreateWeek2(shared, interactions);
        SO_WeekDefinition week3 = CreateWeek3(shared, interactions);
        SO_WeekDefinition week4 = CreateWeek4(shared, interactions);
        SO_WeekDefinition week5 = CreateWeek5(shared, interactions);

        WriteReadme(new[] { week1, week2, week3, week4, week5 });

        AssetDatabase.SaveAssets();
        AssetDatabase.Refresh();
        Selection.activeObject = week1;
    }

